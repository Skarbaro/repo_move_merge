public class DifNumbers {

   public static void main(String[] args) {
      int num1 = 5;
	  int num2 = 15; 
	  int dif;
      dif = num1 - num2;
      System.out.println("Sum of these numbers: " + dif);
   }
   
   public static void main(String[] args) {
      int sum1 = 5; 
	  int sum2 = 15;
	  int sum;
      sum = sum1 - sum2;
      System.out.println("Sum of these numbers: " + sum);
   }
}